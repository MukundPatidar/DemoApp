package Experiments;

class DataType {
    public static void main (String args []) {
        int a = -123556;
        long b = 45455555;
        char ch1 = 'M';
        char ch2 = 65;
        float x = 13.5f;
        String name="Mukund Patidar";
        System.out.println("This is an Integer: "+ a);
        System.out.println("This is also an Integer: "+ b);
        System.out.println("This is a character: "+ ch1);
        System.out.println("This is also a character: " + ch2);
        System.out.println("This is a floating point Number: "+ x);
        System.out.println("This is a string: "+ name);
    }
}